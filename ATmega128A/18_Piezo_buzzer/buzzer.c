void music_player(int *tone, int *Beats)
{
	while(*tone != '/0')
	{
		OCR1A = *tone;
		delay_ms(*Beats);
		tone++;
		Beats++;
		OCR1A = 0;
		_delay_ms(10);		
	}
	return;
}  
// Timer3 위상교정 PWM
void init_speaker(void)
{
	DDRB |= (1 << 5); 
	TCCR1A = (1 << COM1A0);
	TCCR1B = (1 << WGM12) | (1 << CS11);
	TCCR1C = 0;
	OCR1A = 0;
	
	return;
}

void beep(int repeat)
{
	int  i;
	
	for(i=0; i < repeat; i++)
	{
		OCR1A = 500;  // 0.00025sec (250us) : 0.0000005 * 500
		_delay_ms(200);
		OCR1A = 0;
		_delay_ms(200);
	}
}    

void siren(int repeat)
{
	int i, j;
	
	OCR1A = 900;
	
	for(i=0; i < repeat; i++)
	{
		for(j=0; j < 100; j++)
		{
			OCR1A += 10;
			_delay_ms(20);
		}
		for(j=0; j < 100; j++)
		{
			OCR1A -= 10;
			_delay_ms(20);
		}
	}
}

void rrr(void)
{
	int i;
	
	for(i=0; i<20; i++)
	{
		OCR1A = 1136;
		_delay_ms(100);
		OCR1A = 0;
		_delay_ms(20);
	}
}

